//Formas de crear variables en JS (let, var, const)


//var
var helloWorld = "Hello World" // El método var es el método que NO debemos utilizar.
console.log(helloWorld)

helloWorld="Hola de nuevo, JavaScript"
console.log(helloWorld)//Hemos modificado el valor de la variable

//let

let helloWorld2 = "Hello World2" // El método var es el método que NO debemos utilizar.
console.log(helloWorld2)

helloWorld2="Hola de nuevo, JavaScript2"
console.log(helloWorld2)//Hemos modificado el valor de la variable


//const

const helloWorld3 = "Hello helloWorld3" // El método var es el método que NO debemos utilizar.
console.log(helloWorld3)

// helloWorld3="Hola de nuevo, JavaScript3" [No se puede reasignar el valor a una variable definida por const ]
console.log(helloWorld3)  